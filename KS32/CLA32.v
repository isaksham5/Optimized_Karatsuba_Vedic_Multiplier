// ===============================
// 32-bit CLA using 16-bit blocks
// ===============================
module CLA32(
    input  [31:0] A, B,
    input  cin,
    output [31:0] sum,
    output cout
);

    wire [1:0] Gg, Pg;
    wire [2:0] Cblk;
    wire [1:0] dummy_cout;

    assign Cblk[0] = cin;

    cla_16bit b0(
        .A(A[15:0]),
        .B(B[15:0]),
        .cin(Cblk[0]),
        .sum(sum[15:0]),
        .cout(dummy_cout[0]),
        .Gg(Gg[0]),
        .Pg(Pg[0])
    );

    cla_16bit b1(
        .A(A[31:16]),
        .B(B[31:16]),
        .cin(Cblk[1]),
        .sum(sum[31:16]),
        .cout(dummy_cout[1]),
        .Gg(Gg[1]),
        .Pg(Pg[1])
    );

    assign Cblk[1] = Gg[0] | (Pg[0] & Cblk[0]);
    assign Cblk[2] = Gg[1] | (Pg[1] & Gg[0]) | (Pg[1] & Pg[0] & Cblk[0]);

    assign cout = Cblk[2];

endmodule
